#include "../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int nb;

	if (fd < 0 || fd > 256)
		return;
	nb = (unsigned int)n;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -n;
	}
	if (nb > 9)
	{
		ft_putnbr_fd((nb / 10), fd);
		ft_putnbr_fd((nb % 10), fd);
	}
	else
		ft_putchar_fd('0', fd);
}

int		main(void)
{
	ft_putnbr_fd(2147483646, 1);
}
